import math  # Modulo necesario para Funciones de math xd
# Redondea el numero
print(round(3.5))
# Valor absoluto
print(abs(-525))
# Techo
print(math.ceil(3.4))
# Piso
print(math.floor(1.4))
# Si es un numero o no Si es falso es un numero
print(math.isnan(34))
# Potenciacion
print(math.pow(45, 32))
# Raiz cuadrada
print(math.sqrt(3))
